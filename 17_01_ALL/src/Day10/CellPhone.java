package Day10;

public class CellPhone {

	//속성(멤버변수)
	String Model;	//모델명
	String Number; 	//전화번호
	int chord;		//화음
	
	public String getNumber() {
		return Number;
	}
	public void setNumber(String number) {
		Number = number;
	}
	public String getModel() {
		return Model;
	}
	public int getChord() {
		return chord;
	}

	
	
	
	
}
